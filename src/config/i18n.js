export function t(lang, key, params) {
  const table = dict[lang] || dict[DEFAULT_LANG];
  let str = table[key] ?? dict[DEFAULT_LANG][key] ?? key;
  if (params) {
    str = str.replace(/\{(\w+)\}/g, (m, p) => (params[p] !== undefined ? params[p] : m));
  }
  return str;
}

export function normalizeLang(value) {
  return LANGS.includes(value) ? value : DEFAULT_LANG;
}

function parseCookies(header) {
  const out = {};
  if (!header) return out;
  for (const part of header.split(';')) {
    const idx = part.indexOf('=');
    if (idx === -1) continue;
    out[part.slice(0, idx).trim()] = decodeURIComponent(part.slice(idx + 1).trim());
  }
  return out;
}

/**
 * Express middleware: resolves the active language from the `lang` cookie and
 * exposes `t`/`lang`/`roleLabel` to controllers (req.*) and views (res.locals.*).
 */
export function i18nMiddleware(req, res, next) {
  const cookies = parseCookies(req.headers.cookie);
  const lang = normalizeLang(cookies.lang);
  const translate = (key, params) => t(lang, key, params);

  req.lang = lang;
  req.t = translate;
  res.locals.lang = lang;
  res.locals.t = translate;
  res.locals.currentPath = req.originalUrl; // for the language switcher to return here
  res.locals.roleLabel = (role) => t(lang, `role.${role}`);

  // Localized product fields: show RO value when the active language is RO and
  // a translation exists; otherwise fall back to the original (RU) value.
  res.locals.pName = (p) => (lang === 'ro' && p.name_ro ? p.name_ro : p.name);
  res.locals.pDesc = (p) => (lang === 'ro' && p.description_ro ? p.description_ro : p.description);
  next();
}
